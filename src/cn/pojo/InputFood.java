package cn.pojo;

public class InputFood {
	private String FoodEntryNum;
	private String FoodNumber;
	private String FoodName;
	private String FoodPurchasePrice;
	private String InNumber;
	private String Cost;
	private String Supplier;
	private String AllTotal;
	private String InDate;
	public String getFoodEntryNum() {
		return FoodEntryNum;
	}
	public void setFoodEntryNum(String foodEntryNum) {
		FoodEntryNum = foodEntryNum;
	}
	public String getFoodNumber() {
		return FoodNumber;
	}
	public void setFoodNumber(String foodNumber) {
		FoodNumber = foodNumber;
	}
	public String getFoodName() {
		return FoodName;
	}
	public void setFoodName(String foodName) {
		FoodName = foodName;
	}
	public String getFoodPurchasePrice() {
		return FoodPurchasePrice;
	}
	public void setFoodPurchasePrice(String foodPurchasePrice) {
		FoodPurchasePrice = foodPurchasePrice;
	}
	public String getInNumber() {
		return InNumber;
	}
	public void setInNumber(String inNumber) {
		InNumber = inNumber;
	}
	public String getCost() {
		return Cost;
	}
	public void setCost(String cost) {
		Cost = cost;
	}
	public String getSupplier() {
		return Supplier;
	}
	public void setSupplier(String supplier) {
		Supplier = supplier;
	}
	public String getAllTotal() {
		return AllTotal;
	}
	public void setAllTotal(String allTotal) {
		AllTotal = allTotal;
	}
	public String getInDate() {
		return InDate;
	}
	public void setInDate(String inDate) {
		InDate = inDate;
	}
	
	
}
